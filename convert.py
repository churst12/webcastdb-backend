result = []
times = ["0:00", "2:02",
"2:03", "3:54",
"3:54", "23:43",
"23:44", "26:19",
"26:20", "29:09",
"29:09", "41:41",
"41:42", "50:04",
"50:05", "54:21",
"54:22", "59:39",
"59:40", "1:06:20",
"1:06:21", "1:10:13",
"1:10:14", "1:15:03",
"1:15:04", "1:16:15",
"1:16:16", "1:19:36",
"1:19:37", "1:35:00"]
for time in times:
	arr = time.split(':')
	if len(arr) == 2:
		secs = int(arr[0])*60 + int(arr[1])
	else:
		secs = int(arr[0])*3600 + int(arr[1])*60 + int(arr[0])

	result.append(secs)
